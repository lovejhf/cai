package com.ntcai.ntcc.bean;

public class FooTypeVo  {
    private String name;
    private String url;

    public FooTypeVo(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
